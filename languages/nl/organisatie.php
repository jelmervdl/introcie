<h2>Cover</h2>
<p>Cover is de studievereniging voor de opleidingen Kunstmatige Intelligentie en Informatica. Als studievereniging organiseren we het hele jaar door veel leuke studiegerelateerde en ontspannende activiteiten. Zo gaan we regelmatig langs bij bedrijven om te kijken hoe het er op de werkvloer aan toe gaat en komen er regelmatig sprekers langs die een interessant verhaal vertellen. Daarnaast hebben we natuurlijk ook een boel ontspannende activiteiten zoals een maandelijkse borrel, spellenavond en vele andere leuke, originele activiteiten waarbij je je studiegenoten op een andere manier leert kennen dan in de collegebank. Ten slotte kun je via ons ook korting krijgen op je boeken.</p>
<p>Verder heeft Cover ook haar eigen kamer (de SLACK) op de universiteit. Hier kun je tussen de colleges door even rustig bijkomen en gezellig even wat eten, drinken en chillen met je studiegenoten. Ook als je vast zit met opdrachten en dergelijke zijn hier altijd wel wat ouderejaars aanwezig die je even kunnen helpen met je problemen. Hopelijk zullen we jullie hier nog veel terug zien!</p>

